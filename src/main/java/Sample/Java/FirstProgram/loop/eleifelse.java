// Nested If statement.

public class eleifelse {

    public static void main(String args[]) {
        int x = 301;
        int y = 103;

        if( x == 30 ) {
            if( y == 10 ) {
                System.out.print("X = 30 and Y = 10");
            }
        }
    }
}