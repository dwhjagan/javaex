package Sample.Java.FirstProgram.valuclass;

public class valueclass {

    public static void main(String args[]) {
        Integer x = 5;

        // Returns byte primitive data type
        System.out.println( x.byteValue() );   //byteValue

        // Returns double primitive data type
        System.out.println(x.doubleValue());   //doubleValue

        // Returns long primitive data type
        System.out.println( x.longValue() );   //longValue
    }
}